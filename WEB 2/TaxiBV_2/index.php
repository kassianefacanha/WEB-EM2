				<h1>OBSERVAÇÕES COM AS LEIS</h1>
				
				<p>Posted by <a href="index.html">leis de taxi</a></p>

                          <center> <strong>LEIS DISCIPLINARES DO SERVIÇO DE TÁXIS</strong></center><br> <br>
<img src="images/thumb-180650-taxi-resized.jpg" width="98%" height="30%" />
<font color="" style="text-align:justify;">
                            Disciplina o Serviço de automóveis de Aluguel (TAXI) e dá outras providências<br>

<p>Art. 1º. -Os veículos de aluguel destinados ao transporte individual de passageiros
(TAXI), quando na via pública, estão permanentemente a disposição do público, não
podendo seus condutores recusar a prestação de serviços salvo quando se tratar de
pessoas perseguidas pela polícia ou pelo clamor público sob a acusação de prática
de crimes ou quando se tratar de pessoas embriagadas ou em estado que permita
prever venha a causar danos ao veículo ou ao condutor.
	</p>	<p>
Art. 2º. - O veículo de aluguel não é obrigado ao transporte de animais, podendo
fazê-lo mediante consentimento do condutor e sob a responsabilidade do
passageiro, observando entretanto, a tarifa em vigor, sem qualquer acrescimo no
preço.
	</p>	<p>
Art. 3º. - São deveres dos condutores de veículo de aluguel (TAXI), sem prejuízo das
obrigações previstas no Código Nacional de Trânsito:<br>
a) usar de maior correção e urbanidade para com os passageiros;<br>
b) obedecer ao sinal de parada feito por pessoas que desejam utilizar o veículo,
sempre que circular com com a indicação " LIVRE ";<br>
c) seguir o itinerário mais curto, salvo por determinação expressa do passageiro
ou da autoridade de trânsito;<br>
d) indagar o destino do passageiro no interiro do veículo, somente depois do
mesmo estar acomodado, exceto em se tratando de serviço noturno,
compreendido entre às 22 horas de um dia e às 05:00 horas do dia imediato;<br>
e) verificar, ao fim de cada corrida se foi deixado algum objeto no vículo,
entregando-o caso afirmativo mediante contra-recibo e dentro do prazo de 24
horas na Repartição de Transito ou na Delegacia de Polícia mais próxima;<br>
f) somente deter o veículo para embarque ou desembarque do passageiro,
junto ao meio-fio ou guia, de maneira a não prejudicar a livre circulação de
veículos;<br>
g) manter o veículo limpo e asseado.<br>
	</p>	<p>
Art. 4º. - É vedado aos motoristas de veículos de aluguel, sem prejuízo das
proibições decorrentes de outros dispositivos legais e regulamentares:
a) abandonar o veículo nos locais de estacionamento ou fora deles, sem motivo
justificado;<br>
b) reduzir ou suspender, intensionalmente, a marcha permitida pelas condições
de tráfego;<br>
c) fazer-se acompanhar por pessoas estranhas ao serviço;<br>
d) importunar os transeuntes, instando-os pela aceitação dos seus serviços;<br>
e) Dormir ou fazer refeições no veículo;<br>
f) conduzir passageiros com a indicação "LIVRE";<br>
g) continuar a serviço do passageiro que pretendia fazer ficar o veículo
estacionado em lugar não permitido;<br>
h) Dirigir gracejos ou ofensas a apssageiros ou transeguntes, ou usar palavras
ou gestos contrários aos bons costumes;<br>
i) violar o taxímetro;<br>
j) cobrar acima do registra o taxímetro;<br>
k) dirigir com excesso de lotação.<br>
	</p>	<p>
Art. 5º. - Os veículos de aluguel:<br>
a) São obrigados a fazer o transporte de bagagens dos passageiros, desde que
pelas suas dimensões, natureza e peso não venha a prejudicar o veículo;<br>
b) poderão, quando o passageiro desejar, permanecer à sua disposição onde o
estacionamento em geral for permitido, contanto que tenha o taximétro em
funcionamento com BANDEIRA LIVRE arreado;<br>
c) somente poderão ter baixada a bandeira do taxímetro, depois do passageiro
acomodado e levantada após terminado o serviço e com o passageiro ciente
da quantia a pagar. Excetuando-se os casos de chamada à distância;<br>
d) só poderão ser registrados ou licenciados como táxis, os veículos que
contarem até dez (10) anos da fabricação, desde que tenham condições
técnicas de funcionamento;<br>
e) deverão ter escrito nas portas, em letras de imprensa, nas dimenões de
0,20m de altura por 0,10m de largura, a disignação "TAXI";<br>
f) deverão portar em local de fácil acesso e pronta utilização, extintor de
incêndio com capacidade mínima de um (1) quilograma de carga;<br>
g) deverão ter instalados cintos de segurança, em número correspondente à
capacidade de pessoas transportáveis, de acordo com as especificações do
CONTRN, bem como ter internamente, em local bem visível e em letras de
imprensa, a inscrição: "USE O CINTO DE SEGURANÇA";<br>
h) se de duas portas apenas (TAXIS MIRINS), ficam obrigados à retirada do
banco dianteiro direito e do suporte de fixação do mesmo;<br>
i) não podem trazer na aprte externa da carroceria ou dos vidros qualquer
enfeite que venha alterar as características do veículo.<br>
</p>	<p>
Art. 6º. Nas proximidades de hotéis, casas de diversões e de estações de embarque
e desembarque, feito o sinal à fila de taxis, os motoristas são obrigados a conduzilos
em coluna até onde se encontram os passageiros, sendo proibida qualquer
combinação para escolha de passageiros, por intermédio de porteiros, carregadores
ou outras pessaos.
	</p>	<p>
Art. 7º. - É vedado aos passageiros sugerir ou solicitar aos motoristas qualquer ação
ou omissão que implique em desrespeito às normas do trânsito, ficando responsável
o motorista pelo não cumprimento desta disposição.
	</p>	<p>
Art. 8º. - Fica limitada a cinco (5) e três (3) passageiros, respectivamente, a lotação
máxima dos taxis convencionais e mirins.
	</p>	<p>
Art. 9º. - É proibido ao motorista, cobrar a qualquer título, remineração de retorno ao
passageiro desembarcado.
	</p>	<p>
Art. 10º. - Fica facultado o contrato de aluguel para serviços intermunicipais e
interestaduais.
	</p>	<p>
Art. 11º. - O registro ou licenciamtno de taxi, resalvados os autonomos, somente
será concedido em nome de firmas individuais ou coletivas, devidamente inscritas no
competente registro do Comércio, e que possuem no mínimo cinco (5) veículos.

§ 1º. - Não será concedida a renovação de licenciamento a partir de 1 º de janeiro de
1973, aos atuais taxis que não satisfaçam às exigências estabelecidas neste artigo.
§ 2º. - De acordo com o artigo de Consolidação das Leis do Trabalho, não será
concedido o Registro ou Licenciamento de taxis sem que sejam exibidas as provas
de quitação da Contribuíção
	</p>	<p>
Art. 12º. - As firmas individuais ou coletivas, de que trata o art. 11, deverão ter uma
garagem com área coberta não inferiro a 200,00m² (duzentos metros quadrados),
com corpo de mecânicos e auxiliares especializados.
Art. 1º. - Fica revogado e sem nenhum efeito o artigo 12 da Lei nº 4164, de 03 de
maio de 1973.
	</p>	<p>
Art. 13º. - Fica assegurado ao proprietário de taxi, desde que comunique
previamente ao DETRAN, o direito de substituir o seu veículo por outro de modelo
mais novo, enquanto seja o substituído transferido de categoria.
	</p>	<p>
Art. 14º. - É permitido a transferência de propriedade de taxi, desde que não
implique no aumento do número de taxis de aluguel registrados.
Paragrafo ùnico - O novo proprietário do taxi transferido fica obrigado a nova licença
de funcionamento, mediante o pagamento de dois (2) salários mínimos regionais.
	</p>	<p>
Art. 15º. - As infrações cometidas contra este regulamento e que não sejam
previstas em outros diplomas legais, serão punidas com trinta por cento (30%) do
salário mínimo regional.
Parágrafo ùnico - Nos casos de reincidência específica, comulação de infrações, ou
que envolvam outros aspectos delituosos de natureza grave, previstos neste e em
outros diplomas legais, poderão ser aplicadas, concomitantemente as penalidades
de cassação de matrícula do motorista e/ou transferência de categoria do veículo.
	</p>	<p>
Art. 16º. - A partir de 1º de julho do corrente ano ficam obrigados os taxis a portar
cópia do presente Regulamento impressa e plastificada, devidamente autenticada
pelo Departamento Estadual de Trânsito, em local visível e de fácil menuseio por
parte dos passageiros.
	</p>	<p>
Art. 17º. - Os casos omissos serão resolvidos pelo Secretário Municipal de Serviços
Urbanos.
	</p> 	<p>
Art. 18º. - Esta Lei entrará em vigor na data de sua publicação, revogadas as
disposições em contrário.</p>  
