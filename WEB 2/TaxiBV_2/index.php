				<h3>Dicas de Equipamentos exigidos</h3>
				<p>
<font color="" style="text-align:justify;">
				<a href="#"><img src="images/img_automaistaxi.jpg" width="100%" height="30%" /></a>
				<strong><center>Equipamentos</center></strong>

O equipamento imprescindível para o exercício da atividade é o
carro. Para serem utilizados no serviço de transporte de
passageiros na forma de táxi, em geral os carros devem
satisfazer além das exigências do CTB e Legislação correlata,
as seguintes exigências estabelecidas pelos municípios:<br><br>
I. Encontrar-se em bom estado de conservação e
funcionamento;<br>
II. Utilizar a pintura padronizada e demais caracterização para
táxi exigida pelo município.<br>
III. Fabricação não superior a 08 (oito) anos;<br>
IV. Estarem equipados com:<br><br>
a) extintor de incêndio de capacidade proporcional à categoria
do veículo Táxi e no modelo aprovado por resolução do
Conselho Nacional de Trânsito;<br>
b) taxímetro ou aparelhos registradores, em modelo aprovados,
devidamente aferidos e lacrados pela autoridade competente;<br>
c) caixa luminosa com a palavra “TÁXI”, sobre o teto, dotada de
dispositivo que apague sua luz interna automaticamente,
quando do acionamento do taxímetro;<br>
d) dispositivo que indique a situação “livre” ou “em
atendimento”;<br>
e) cintos de segurança em perfeitas condições;<br>
f) luz de freio elevada (brake light), na parte inferior interna
(vidro traseiro);<br>
V. conterem nos locais indicados:<br><br>
a) a identificação do proprietário e do condutor;<br>
b) o dístico “É PROIBIDO FUMAR”;<br>
c) o número da placa de registro pintado nas portas dianteiras e
a parte externa do teto, logo acima do vidro traseiro e
internamente no painel;<br>
d) identificação externa da empresa proprietária, através de
siglas e símbolos previamente aprovados;<br><br>
<b>Nota:</b> Estas exigências podem mudar de cidade para cidade,
tais como número de portas do veículo, potência do motor,
ar-condicionado, etc.	</font>							
